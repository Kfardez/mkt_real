            <h4><u>REGLEMENT INTERIEUR</u></h4>
            <br>
            <h4><i><u>Préambule</u></i></h4>
          </div>
          <br>
          <p>L’Ecole a pour but :</p>
          <p>- de donner aux élèves des connaissances et des habitudes de travail</p>
          <p>- de favoriser leur épanouissement et de leur apprendre à vivre au sein d’une collectivité.</p>
          <br>

          <div style="box-shadow: 3px 3px blue, -1em 0 .4em olive;text-align:center">
            <p>Le Lycée-Collège Marie Kə Tal est un Établissement secondaire Catholique créé par les Frères Maristes des Écoles(FMS) à la demande du Diocèse de Sarh. Il est la continuité du travail amorcé par le Collège Communautaire Jean Paul II. Il est ouvert à tous, sans distinction d’ethnie, de religion ou de sexe et vise essentiellement la qualité et la formation intégrale de tous les jeunes.</p>
            <p>Tout élève qui s’inscrit au<b> LCMKT</b> le fait librement. Il s’engage donc personnellement à y vivre dans l’esprit de notre établissement et à observer scrupuleusement ledit règlement afin d’assurer à ses camarades et à lui- même les meilleures conditions de travail, de vie communautaire et d’éducation dans le respect de ses Professeurs et éducateurs, du Personnel, de tous ses camarades et aussi du bien matériel.</p>
          </div>
          <br>


        <h5 style="color: rgb(0, 0, 224)"><b><u>Chapitre I </u></b> : ORGANISATION ADMINISTRATIVE.</h5>
        <p>Le <b>L.C.M.K.T</b>  dispose d’un organigramme administratif qui suit : </p>
        <ul>
            <li>Le Proviseur, comme chef d’Etablissement ;</li>
            <li>Le Proviseur adjoint </li>
            <li>Un Econome ;</li>
            <li>Un Censeur ;</li>
            <li>Un Directeur des Études </li>
            <li>Les Surveillants généraux ;</li>
            <li>Les Professeurs principaux et ;</li>
            <li>Les Chefs des clases ainsi que leurs adjoints.</li>

        </ul>
        <h5 style="color: rgb(0, 0, 224)"><b><u>Chapitre II </u></b> : DE LA DISCIPLINE ET DE LA PONCTUALITÉ.</h5>
        <h6><b>Article 1 :</b></h6>
        <p>Les cours au L.C.M.K.T commencent de 7h30 à 12h20 intercalés de deux récréations : la 1ère récréation est fixée de 9h10 à 9h30 et la 2ème  , de 11h10 à 11h30 pour la matinée et l’après-midi de 16h00 à 17h50 pour toute l’année. Lundi la levée des couleurs à 7h20 et la décente le samedi à 11h10</p>
        <h6><b>Article 2 :</b></h6>
        <p>Les élèves sont ténus d’être présents et ponctuels tous les jours ouvrables dans l’Etablissement avant le 1er coup de cloche qui est fixé à 7h20.</p>
        <h6><b>Article 3 : EXACTITUDE </b></h6>
        <p>Au premier coup de cloche, les élèves se dirigent immédiatement vers leur salle de classes respectives et prêts à commencer les cours. Le silence doit régner même à l’absence d’un professeur. Au second coup de cloche les cours ou les études commencent. Tout élève qui arrive après le deuxième coup de cloche est considéré comme en retard et doit se présenter à la surveillance automatiquement.</p>

        <h5 style="color: rgb(0, 0, 224)"><b><u>Chapitre III </u></b> : DES INTERDITS</h5>
        <h6><b>Article 1 :</b></h6>
        <p>Il est strictement interdit d’apporter au sein de l’Etablissement les Armes à feu ou blanches, alcool de quelque nature que ce soit ; tout contrevenant sera purement et simplement exclu de l’Etablissement.</p>
        <p>Il est aussi interdit de :</p>
        <ul>
            <li>de sortir hors de la classe ou en dehors de l’enceinte de l’Etablissement avant la fin des cours </li>
            <li>d’aller à la toilette, d’aller boire de l’eau pendant les cours et après chaque récréation   sauf lorsque le besoin est avéré et constaté par le Professeur </li>
            <li>de trainer dans la cour après le coup de cloche marquant la fin de chaque récréation.</li>
            <li>de passer la récréation en classe ;</li>
            <li>de manger en classe pendant ou après les cours ;</li>
            <li>de jeter des papiers, ordures, emballages, etc. dans la cours ou dans la classe ;</li>
            <li>de marcher ou sauter sur les gazons ou fleurs.</li>
            <li>Tout contrevenant sera récupéré par les Surveillants et soumis à des travaux manuels (nettoyage de la cour, arrosage des gazons et fleurs etc. )</li>
            <li>L’école étant par excellence un lieu de brassage, ou l’on apprend à lire, à écrire et à s’exprimer alors il est formellement interdit de :</li>
            <ul style="list-style-type: none;">
                <li>- parler le patois au sein de l’Etablissement ; seuls l’Anglais, le Français et l’Arabe littéraire sont autorisés ;</li>
                <li>- de gaspiller de l’eau pour se laver les pieds à la fin de l’heure.</li>
            </ul>
        </ul>

                <p>Tout contrevenant sera récupéré par les surveillants et copiera 1000 fois la version française de sa phrase prononcée avant d’avoir accès en salle.</p>

                <h5 style="color: rgb(0, 0, 224)"><b><u>Chapitre IV </u> </b> : DE LA TENUE SCOLAIRE</h5>
                <h6><b>Article 1 :</b></h6>
                <p>L’uniforme de l’Etablissement est le bleu-marine sur kaki pour tous les élèves.</p>
                <h6><b>Article 2 :</b></h6>
                <p>Le port de la tenue est obligatoire pendant toute la période de cours, d’études et à toutes les cérémonies organisées par le L.C.M.K.T. Les élèves doivent enfiler la chemise depuis la maison jusqu’au L.C.M.K.T et du L.C.M.K.T jusqu’à la maison.</p>
                <h6><b>Article 3 :</b></h6>
                <p>Pendant les travaux manuels organisés par le L.C.M.K.T, les élèves doivent venir en tenue de sport dont les couleurs sont déterminer par les chargés de sport</p>
                <h6><b>Article 4 :</b></h6>
                <p>Toute extravagance vestimentaire (blouson, jacket, coiffure extraordinaire, ongle artificielle, bague, pendentifs, casquettes (chapeau)) est interdite.</p>
                <h6><b>Article 5 :</b></h6>
                <p>Tout contrevenant à l’article 1, 2,3 et 4 s’expose à des sanctions allant de 3 jours à une semaine de suspension de cours. </p>

                <h5 style="color: rgb(0, 0, 224)"><b><u>Chapitre V </u> </b> : LA  LEVÉE DES COULEURS</h5>
                <h6><b>Article 1 :</b></h6>
                <p>La levée des couleurs s’effectue chaque Lundi matin à 7h20 et la descente est faite chaque Samedi à 11h10</p>
                <h6><b>Article 2 :</b></h6>
                <p>Tous les élèves sont tenus d’être présents et disciplinés. </p>

                <h5 style="color: rgb(0, 0, 224)"><b><u>Chapitre VI </u> </b> : LA  LEVÉE DES COULEURS</h5>
                <h6><b>Article 1 :</b></h6>
                <p>Sont considérés comme absents les élèves qui ne sont pas à leur place au coup de cloche, qu’il soit pendant les heures d’inter-classes ou les récréations.</p>
                <h6><b>Article 2 :</b></h6>
                <p>Tout élève qui se trouve en dehors de l’Etablissement après le coup de cloche de 7h20 minutes est considéré comme absent et il est contraint à rentrer chez lui auprès de ses parents.</p>
                <h6><b>Article 3 :</b></h6>
                <p>Tout élève qui a été en retard est considéré comme absent, et avant d’entrer en classe ou en étude, se présentera à son surveillant afin de donner les raisons de son absence et de demander un billet d’accès. De même, un élève exclu pendant les heures de cours doit se présenter à la surveillance pour obtenir un billet d’accès en salle. En attendant le début de l’heure suivante, le surveillant le soumet à des travaux manuels. </p>
                <h6><b>Article 4 :</b></h6>
                <p>Tout élève absent à un devoir obtient de facto la note 00/20 sauf si un justificatif valable est présenté. </p>
                <h6><b>Article 5 :</b></h6>
                <p>Tout élève renvoyé pour indiscipline ne prendra pas part aux devoirs faits pendant cette période de suspension et obtient 00/20.</p>
                <h6><b>Article 6 :</b></h6>
                <p>: Le temps des études est le moment indiqué pour un travail personnel, sérieux. Pour ce faire, tout élève doit être présent pendant l’intégralité de ce temps. Le silence doit régner durant les études dans les locaux et aux alentours. Par conséquent, les élèves doivent prévoir tous les instruments et documents nécessaires pour le travail. Le chef de classe ne peut autoriser un élève à sortir de l’étude qu’après une heure de temps. Cette permission ne sera accordée qu’à un seul élève une seule fois. Cela pour des cas avérés. Le chef de classe peut encourir des sanctions pour son laxisme. Chaque élève est tenu de rester à sa place durant les études. Les travaux de groupe sont strictement interdits pendant les études, sauf sur autorisation du professeur ou le Directeur des Études / Censeur. </p>
                <h6><b>Article 7 :</b></h6>
                <p>Tout élève contrevenant à l’encontre   des articles cités ci-haut notamment l’article 1 et 2 de chapitre VI est sanctionné par le renvoi pendant toute la journée et notification sera faite aux parents par messagerie écrite</p>

                <h5 style="color: rgb(0, 0, 224)"><b> <u>Chapitre VII </u> </b> : DE L’ENTRETIEN ET DU BON SOIN DES MATERIELS</h5>
                <p>La vie communautaire au L.C.M.K.T exige le respect et l’entretien des biens mobiliers et immobiliers ainsi que des meubles, matériel et documents mis à notre disposition. </p>
                <h6><b>Article 1 :</b></h6>
                <p>Chacun est responsable de l’entretien de tout ce qui est à sa disposition et  par conséquent, il doit le garder en bon état : livres, journaux, Tabourets, Tables, fenêtres, portes, bâtiments, cuillères, fourchettes, assiettes, verres, robinets, gobelets---</p>
                <h6><b>Article 2 :</b></h6>
                <p>L’eau c’est la vie et pour cela chacun doit prendre des dispositions nécessaires afin d’éviter le gaspillage d’eau.</p>
                <h6><b>Article 3 :</b></h6>
                <p>Tout document, tout matériel abîmé, perdu ou dégradé sera remplacé ou réparé par l’intéressé.</p>

                <h5 style="color: rgb(0, 0, 224)"><b> <u>Chapitre VIII </u> </b> : ENSEIGNEMENT RELIGIEUX ET ENGAGEMENT</h5>
                <h6><b>Article 1 :</b></h6>
                <p>l’enseignement religieux est l’identité des établissements d’enseignement catholique. Le L.C.M.K.T ne peut épargner. Tout élève de notre établissement n’a pas le droit de négliger ou refuser cette discipline qui figure bel et bien dans notre emploi du temps de chaque classe.</p>
                <h6><b>Article 2 :</b></h6>
                <p> Chaque matin avant de commencer les cours, tous les élèves doivent se conformer à la tradition du collège et par respect de la prière se tenir debout, même les élèves d’autres confessions religieuses pour accompagner les autres dans leur prière pour lancer la journée.</p>
                <h6><b>Article 3 :</b></h6>
                <p>Tout contrevenant à l’article 1 et 2 du chapitre VIII s’expose à des sanctions allant de l’exclusion temporaire à l’exclusion définitive. </p>

                <h5 style="color: rgb(0, 0, 224)"><b> <u>Chapitre IX </u> </b> : DES DISPOSITIONS FINALES</h5>
                <p>Toute vie  en société ne peut être heureuse, belle et harmonieuse que lorsqu’elle est régie par des lois et que tout le monde s’y conforme aux dites lois. De ce fait, tout élève inscrit au L.C.M.K.T doit se conformer au présent règlement intérieur. Il doit se montrer poli et courtois envers tous dans un esprit de camaraderie et de civilité avérée.</p>
                <p><b>Sont considérées comme fautes mineures et sanctionnées par le chef d’Etablissement ; allant de la suspension temporaire de cours au blâme :</b></p>

                <ul>
                    <li>Le retard au cours ;</li>
                    <li>Le trouble pendant le cours ;</li>
                    <li>La tenue non-réglementaire ;</li>
                    <li>Le port des objets interdits ou dangereux </li>
                    <li>Les courtes absences injustifiées ;</li>
                    <li>L’insolence, les paroles et les comportements impudiques.</li>
                </ul>

                    <p><b>Sont considérées comme fautes majeures, des fautes qui sont sanctionnées par l’exclusion définitive de l’Etablissement sans conditions.</b></p>

                <ul>
                    <li style="color:red;">Les bagarres ;</li>
                    <li>Les agressions de toutes sortes ;</li>
                    <li>La consommation d’alcool et de stupéfiants (Tramadol, drogue---).</li>
                    <li>Le vol, la fraude, la tricherie, le port de téléphone ;</li>
                    <li>Plus de 30 heures d’absences injustifiées est passif d’une peine allant de l’exclusion temporaire à l’exclusion définitive</li>
                    <li>En cas d’une faute grave commise par un élève, le conseil de discipline peut décider de l’exclusion allant de 8 (huit) jours à l’exclusion définitive même en pleine année scolaire selon le degré de la faute. </li>
                    <li>En cas d’insuffisance de travail, l’élève est exclu à la fin de l’année par le conseil d’orientation (CO).</li>
                    <li>Tout élève qui, à la fin d’une année scolaire obtient une moyenne inférieure ou égale <b>à 06/20 est exclu de l’Établissement</b>.</li>
                    <li><b>La moyenne de redoublement est fixée entre 07 à 09.99/20.</b></li>
                    <li><b>La moyenne d’admission au test de passage est fixée entre 09.50/20 à 09.99/20.</b></li>
                    <li><b>Tout élève qui obtient</b> une moyenne supérieure ou égale à 10.00/20 passe en classe supérieure.</li>
                    <li>Les injures, l’insubordination caractérisée envers le staff administratif, le Personnel enseignant, les agents de sécurités et /ou la violation flagrante du présent règlement intérieur.</li>
                </ul>

                    <p><b>NB</b>: Tout manquement à ce règlement et toutes les attitudes négatives ne seront pas tolérés. La Direction du L.C.M.K.T se réserve le droit de statufier sur des cas exceptionnels qui ne figurent pas dans ce règlement, mais qui génèreraient une perturbation du climat du travail au sein de l’établissement.</p>

